@app.cell(hide_code=True)
def _(alt, c1, goal_order, mo):
    # 1. Define the Chart correctly
    _chart = (
        alt.Chart(c1.value)
        .mark_line(interpolate='monotone', strokeWidth=3)
        .encode(
            x=alt.X('age:Q', title='Age'),
            y=alt.Y('count()', title='Number of Clients'),
            tooltip=[
                alt.Tooltip('age:Q', format=',.0f'),
                alt.Tooltip('count()')
            ]
        )
        .properties(
            height=290,
            width=900
        )
        # Use dedicated configuration methods instead of the 'config' dict
        .configure_axis(grid=False)
        .configure_view(stroke=None) 
    )

    chart_age_risk_tolerance = alt.Chart(c1.value).mark_boxplot().encode(
        x=alt.X('age:Q', title='Client Age vs Risk Tolerance'),
        y=alt.Y('risk_tolerance:O', title='', sort='descending')
    ).properties(width=220)

    chart_age_investment_goal = alt.Chart(c1.value).mark_boxplot().encode(
        x=alt.X('age:Q', title='Client Age vs Investment Goal'),
        y=alt.Y('investment_goal:O', title='', sort=goal_order[::-1])
    ).properties(width=220)

    chart_age_liquidity_needs = alt.Chart(c1.value).mark_boxplot().encode(
        x=alt.X('age:Q', title='Client Age vs Liquidity Needs'),
        y=alt.Y('liquidity_needs:O', title='', sort=['high', 'medium', 'low'])
    ).properties(width=220)


    def mark_ke_dount(dataf, prod, scale = 1):

        try:
            percentage = c1.value[prod].sum() / c1.value[prod].count()
        except:
            percentage = 0

        # Donut Layer
        donut = alt.Chart(c1.value).mark_arc(innerRadius=60 * scale, outerRadius=100 * scale).encode(
                color=alt.Color(field=prod, type='nominal', legend=None),
                theta=alt.Theta(aggregate='count', type='quantitative')
        )

        t = alt.Chart(c1.value).mark_text(
            baseline='middle',
            fontSize=18 * scale,
            fontWeight='normal',  # <--- This unbolds the text
            color='black').encode(text=alt.value(f"{percentage:.1%}"))

        return (donut + t).properties(title=prod, width=150* scale, height=150* scale)

    ke_charts = mark_ke_dount(c1, 'ke_equities', scale=0.8) | mark_ke_dount(c1, 'ke_fixed_income', scale=0.8) | mark_ke_dount(c1, 'ke_structured_prods', scale=0.8) | mark_ke_dount(c1, 'ke_derivatives', scale=0.8) | mark_ke_dount(c1, 'ke_alternatives', scale=0.8)

    # Helper to safely get unique values or an empty string if empty
    def get_unique_values(series):
        if series.empty:
            return "N/A"
        return ", ".join([str(v) for v in sorted(series.unique())])

    # Safely get aggregated values
    if c1.value is not None and not c1.value.empty:
        num_clients = len(c1.value)
        total_aum = c1.value.aum_usd.sum()
        mean_age = c1.value.age.mean()
        std_age = c1.value.age.std()
        selected_goals = get_unique_values(c1.value.investment_goal)
        selected_risk_tol = get_unique_values(c1.value.risk_tolerance)
    else:
        num_clients = 0
        total_aum = 0
        mean_age = 0
        std_age = 0
        selected_goals = "N/A"
        selected_risk_tol = "N/A"

    client_summary = mo.md(f"""
    | Metric                 | Value                                                              |
    | :--------------------- | :----------------------------------------------------------------- |
    | **Number of Clients**  | {num_clients}                                                      |
    | **AUM (USD)**          | {total_aum / 100_000_000:0.2f}M                                     |
    | **Age Mean**           | {mean_age:.2f}                                                     |
    | **Age SD**             | {std_age:.2f}                                                      |
    | **Selected Goals**     | {selected_goals}                                                   |
    | **Selected Risk Tol.** | {selected_risk_tol}                                                |
    """)


    # 2. Display using hstack
    # Ensure this is the LAST line of the cell or assigned to a variable displayed by marimo
    mo.vstack(
        [
            client_summary,
            _chart,
        ]
    )
    return (
        chart_age_investment_goal,
        chart_age_liquidity_needs,
        chart_age_risk_tolerance,
        ke_charts,
    )


