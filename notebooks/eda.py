@app.cell(hide_code=True)
def _(mo):
    mo.md("""
    **A note on client generation design choices:**

    | Field             | Distribution                                                                                   |
    | :---------------- | :--------------------------------------------------------------------------------------------- |
    | age               | Fat-tailed t-dist (df=4, centre=50, scale=12), rejection-sampled to avoid boundary pile-up     |
    | risk_tolerance    | Discrete weighted choice (1–5) from cfg.risk_tolerance_distribution                              |
    | aum_usd           | Log-normal (exp(Normal(12, 1))), then min-max scaled to configured range                        |
    | liquidity_needs   | Discrete weighted choice from cfg.liquidity_needs_distribution                                   |
    | investment_goal   | Base config weights, age-adjusted: older clients get boosted capital preservation/income, reduced growth |
    | ke_* flags        | Independent Bernoulli per product type, probabilities from cfg.ke_probability                    |


    Two age-dependency layers add realism:
    <br> - Risk tolerance cap — hard ceilings by age bracket (e.g. age >80 → max RT=1), with age_risk_epsilon exception rate
    <br> - Investment goal bias — smooth linear shift starting at age 40 via age_factor = clip((age-40)/40, 0, 1)
    """)
    return


